    fs.run_clean
  FROM fee_source fs
  JOIN public.students s
    ON replace(s.run, '.', '') = fs.run_clean
  LEFT JOIN public.student_guardian sg
    ON sg.student_id = s.id
    AND sg.is_primary = true
)

-- Step 3: UPSERT — insert new fees, update existing ones
-- Preserves: payment_method, num_boleta, mov_bancario, enrollment_id, meta, institucion_financiera, fee_curso
-- Updates:   amount, status (→ paid), payment_date (→ due_date), due_date, notes
INSERT INTO public.fee (
  student_id,
  guardian_id,
  amount,
  due_date,
  payment_date,
  status,
  owner_id,
  year_academico,
  numero_cuota,
  notes
)
SELECT
  r.student_id,
  r.guardian_id,
  r.amount,
  r.due_date,
  r.due_date,       -- payment_date = due_date (punto 5)
  'paid',
  r.owner_id,
  r.year_academico,
  r.numero_cuota,
  r.notes
FROM resolved r
WHERE r.student_id IS NOT NULL
ON CONFLICT ON CONSTRAINT unique_fee_student_guardian_quota
DO UPDATE SET
  amount       = EXCLUDED.amount,
  due_date     = EXCLUDED.due_date,
  payment_date = EXCLUDED.payment_date,
  status       = EXCLUDED.status,
  notes        = EXCLUDED.notes,
  year_academico = EXCLUDED.year_academico,
  updated_at   = now();

